<h1 align="center" >Flood Safety</h1> 
<p align="center">Learn how to keep your family safe:</p><br><br>
<h2>How to Prepare Before a Flood</h2>
<ul><li>Protecting Your Family<br>
    <ul><li> Talk with your family about what to do if a flood watch or warning is issued. Discussing floods ahead of time helps reduce fear, especially for younger children. </li>

   <li>Ensure that every member of your family carries a Safe and Well wallet card. </li>
   <li> Make sure you have access to tv/radio broadcasts:</li>

        <ul> <li>Find an online news app in the Apple Store or Google Play </li>
        <li> Purchase a battery-powered bank</li></ul>
   <li> Find out if you are located in a floodplain, which is considered a Special Flood Hazard Area. If so, you are still eligible for flood insurance. Check with your city or county government (start with the Building or Planning Department) to review the Flood Insurance Rate Maps, published by the Emergency Management Agency . 
   <li> Find out if local streams or rivers flood easily. </li>
   <li>Keep insurance policies, documents, and other valuables in a safe-deposit box. You may need quick, easy access to these documents. Keep them in a safe place less likely to be damaged during a flood. Take pictures on a phone and keep copies of important documents and files on a flashdrive that you can carry with you on your house or car keys. </li></ul>
</li>
<li>protecting Your Pets & Animals<br>

   <ul><li>   Prepare a pet emergency kit for your companion animals. </li>

   <li> Ensure that any outbuildings, pastures, or corrals are protected in the same way as your home. </li>

   <li> If installing or changing fence lines, consider placing them in such a way that your animals are able to move to higher ground in the event of flooding. </li></ul>

<li>Protecting Your Home<br>
  <ul><li>If you live in a floodplain, elevate and reinforce your home to make damage less likely during a flood. </li>

   <li> Check with a professional to:
        <ul type="circle"><li>Raise your furnace, water heater, and electric panel to floors that are less likely to be flooded. An undamaged water heater may be your best source of fresh water after a flood.</li> <li>Install check valves in plumbing to prevent floodwater from backing up into the drains of your home. (As a last resort, when floods threaten, use large corks or stoppers to plug showers, tubs, or basins.) </li> <li>Construct barriers such as levees, berms, and flood walls to stop floodwater from entering the building (if permitted by local building codes). </li> <li>Seal walls in basements with waterproofing compounds to avoid seepage through cracks. </li></ul>
   <li>Use sand bags when flooding is expected:
       <ul type="circle"><li> It takes two people about one hour to fill and place 100 sandbags, creating a wall one foot high and 20 feet long. </li> <li>Make sure you have enough sand, burlap or plastic bags, shovels, strong helpers, and time to place them properly.</li> <li> If a flood is expected, some communities will offer free sandbags to residents. Be sure to watch or listen to the news so you can access these resources. </li></ul>
</li></ul>

<h3>Right Before a Flood</h3><br>
<ul><li>Listen to local area radio, NOAA radio or TV stations for the latest information and updates.</li>
<li>Be prepared to evacuate quickly and know your routes and destinations. Find a local emergency shelter.</li>
<li>Check your emergency kit and replenish any items missing or in short supply, especially medications or other medical supplies. Keep it nearby.</li></ul><br>

<ul type="disc"><li>Then, If You Can, Do This <br>

<ul><li> Fill plastic bottles with clean water for drinking. </li>
<li> Fill bathtubs and sinks with water for flushing the toilet or washing the floor or clothing. </li>
<li>  Fill your car's gas tank, in case you need to evacuate. </li>
<li>  Bring outdoor belongings, such as patio furniture, indoors. </li>
<li>  Turn off propane tanks to reduce the potential for fire.</li> </ul></li>

<li>If You Still Have Time, Do This<br>

<ul><li> Move your furniture and valuables to higher floors of your home. </li>
 <li> Turn off utilities if told to do so by authorities to prevent damage to your home or within the community. If you shut your gas off, a professional is required to turn it back on. </li>
 <li>Unplug small appliances to reduce potential damage from power surges that may occur. </li></ul></li>

<li>If You Have Pets or Livestock<br>

<ul><li>Consider a precautionary evacuation of your animals, especially any large or numerous animals. Waiting until the last minute could be fatal for them and dangerous for you. </li>
<li>Where possible, move livestock to higher ground. If using a horse or other trailer to evacuate your animals, move sooner rather than later. </li>
<li>Bring your companion animals indoors and maintain direct control of them. Be sure that your pet emergency kit is ready to go in case of evacuation. </li></ul></li></ul>
<h2>DURING Flood</h2>

<ul><li><h4>Staying Safe Indoors</h4><br>
 

    <ul><li>Turn off the power and water mains if instructed to do so by local authorities. </li>

    <li>Boil tap water until water sources have been declared safe. </i>

   <li> Avoid contact with floodwater. It may be contaminated with sewage or contain dangerous insects or animals. </li>

    <li>Continue listening to local area radio, NOAA radio or TV stations for the latest information and updates. </li>

   <li> Don’t use gas or electrical appliances that have been flooded. </li>

   <li> Dispose of any food that comes into contact with flood water . </li>
</ul>
 
<li><h4>Staying Safe Outdoors</h4><br>
 

  <ul><li>  Don't walk, swim or drive through floodwater. Just six inches of fast-flowing water can knock you over and two feet will float a car. </li>

  <li>  If caught on a flooded road with rapidly rising waters, get out of the car quickly and move to higher ground. </li>

   <li> Don't walk on beaches or riverbanks. </li>

   <li> Don’t allow children to play in or near flood water. 
</li>
  <li>  Avoid contact with floodwater. It may be contaminated with sewage or contain dangerous insects or animals. </li>

   <li> Stay out of areas subject to flooding. Underpasses, dips, low spots, canyons, washes, etc. can become filled with water. </li></ul>
</li></ul>

<h2>How to Prepare AFTER a Flood</h2>

 
<b>
   <ul> <li>Let friends and family know you’re safe. Register yourself as safe on the Safe and Well website </li>

    <li> If evacuated, return only when authorities say it is safe to do so. </li>

   <li> Continue listening to local news or a NOAA Weather Radio for updated information and instructions. </li>

   <li> Keep children and pets away from hazardous sites and floodwater.</li> </ul>

</b>
<h5>Returning Home Safely</h5>
<ul type="disc">

  <li>  If power lines are down outside your home, do not step in puddles or standing water. Report them immediately to the power company. </li>

  <li>  Follow these tips for inspecting your home’s structure and utilities & systems after a flood. </li>

  <li>  If any gas or electrical appliances were flooded, don’t use them until they have been checked for safety. </li>

  <li>  Dispose of any food that has come into contact with flood water. </li>

  <li>  Take pictures of home damage, both of the buildings and its contents, for insurance purposes. 
</li></ul>

<h5>Cleaning and Repairing Your Home</h5>

    <ul type="disc"><li>Wear protective clothing, including rubber gloves and rubber boots, and be cautious when cleaning up. </li>

   <li> Learn more about how to clean up after a flood, including the supplies you’ll need, how to sanitize food contact surfaces, and how to repair water damage. </li>

   <li> Be careful when moving furnishings or debris, because they may be waterlogged and heavier. </li>

   <li> Throw out items that absorb water and cannot be cleaned or disinfected. This includes mattresses, carpeting, cosmetics, stuffed animals and baby toys. </li>

   <li> Throw out all food, beverages and medicine exposed to flood waters and mud. When in doubt, throw it out. This includes canned goods, plastic utensils, baby bottle nipples and containers with food or liquid that has been sealed shut. </li>

  <li>  Pump out flooded basements gradually (about one-third of the water per day) to avoid structural damage. If the water is pumped out completely in a short period of time, pressure from water-saturated soil on the outside could cause basement walls to collapse. </li>

   <li> Service damaged septic tanks, cesspools, pits, and leaching systems as soon as possible. Damaged sewage systems are health hazards. </li></ul>


